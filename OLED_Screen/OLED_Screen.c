void oled_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(i2c0, 0x3C, buf, 2, false);
}

void oled_data(uint8_t* data, size_t len) {
    uint8_t buf[len + 1];
    buf[0] = 0x40;
    for (int i = 0; i < len; i++) buf[i + 1] = data[i];
    i2c_write_blocking(i2c0, 0x3C, buf, len + 1, false);
}
void oled_init() {
    sleep_ms(100);

    oled_cmd(0xAE); // display off
    oled_cmd(0x20); oled_cmd(0x00); // horizontal addressing
    oled_cmd(0xB0);
    oled_cmd(0xC8);
    oled_cmd(0x00);
    oled_cmd(0x10);
    oled_cmd(0x40);
    oled_cmd(0x81); oled_cmd(0x7F);
    oled_cmd(0xA1);
    oled_cmd(0xA6);
    oled_cmd(0xA8); oled_cmd(0x3F);
    oled_cmd(0xA4);
    oled_cmd(0xD3); oled_cmd(0x00);
    oled_cmd(0xD5); oled_cmd(0x80);
    oled_cmd(0xD9); oled_cmd(0xF1);
    oled_cmd(0xDA); oled_cmd(0x12);
    oled_cmd(0xDB); oled_cmd(0x40);
    oled_cmd(0x8D); oled_cmd(0x14);

    oled_cmd(0xAF); // display ON
}


void oled_clear() {
    for (int page = 0; page < 8; page++) {
        oled_cmd(0xB0 + page);
        oled_cmd(0x00);
        oled_cmd(0x10);

        uint8_t data[128];
        for (int i = 0; i < 128; i++) {
            data[i] = 0x00; // all pixels OFF
        }

        oled_data(data, 128);
    }
}
void oled_draw_char(int page, int column, char ch) {
    // Map character to font index (ASCII 32-126)
    int index = ch - 32;
    if (index < 0 || index >= 96) {
        index = 0; // default to space if out of range
    }

    oled_cmd(0xB0 + page);          // set page
    oled_cmd(column & 0x0F);
    oled_cmd(0x10 | (column >> 4));

    uint8_t buffer[6];

    for(int i = 0; i < 5; i++) {
        buffer[i] = font_5x8[index][i];
    }

    buffer[5] = 0x00; // spacing

    oled_data(buffer, 6);
}

void oled_print_string(int page, const char* str) {
    int col = 0;

    for(int i = 0; str[i] != '\0'; i++) {
        oled_draw_char(page, col, str[i]);
        col += 6;
    }
}

int main()
{
    stdio_init_all();

    // Initialise the Wi-Fi chip
    if (cyw43_arch_init()) {
        printf("Wi-Fi init failed\n");
        return -1;
    }

    // I2C Initialisation. Using it at 400Khz.
    i2c_init(I2C_PORT, 400*1000);
    
    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA);
    gpio_pull_up(I2C_SCL);

    oled_init();
    oled_clear();
    oled_print_string(0, "Line 0");
    oled_print_string(1, "Line 1");
    oled_print_string(2, "Line 2");
    
}
